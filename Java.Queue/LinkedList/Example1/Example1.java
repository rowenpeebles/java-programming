// Create LinkedList in Java

import java.util.LinkedList;

class Example1 {

    public static void main(String[] args) {

        // Create linked list
        LinkedList<String> animals = new LinkedList<>();

        // Add elements to linked list
        animals.add("Dog");
        animals.add("Cat");
        animals.add("Cow");

        System.out.println("Linked List: " + animals);

    }

}
