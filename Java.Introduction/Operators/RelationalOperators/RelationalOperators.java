class RelationalOperators {

    public static void main(String[] args) {

        // Create variables
        int a = 7, b = 11;

        // Value of 'a' and 'b'
        System.out.println("a is " + a + " and b is " + b);

        // '==' Operator
         System.out.println(a == b);

        // '!=' Operator
        System.out.println(a != b);

        // '>' Operator 
        System.out.println(a > b);

        // '<' Operator
        System.out.println(a < b);

        // '<=' Operator
        System.out.println(a <= b);

        // '>=' Operator 
        System.out.println(a >= b);

    }

}
