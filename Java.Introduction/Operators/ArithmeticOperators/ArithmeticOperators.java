class ArithmeticOperators {

    public static void main(String[] args) {

        // Declare Variables
        int a = 12, b = 5;

        // Addition Operator
        System.out.println("a + b = " + (a + b));

        // Subtraction Operator 
        System.out.println("a - b = " + (a - b));

        // Multiplication Operator 
        System.out.println("a * b = " + (a * b));

        // Division Operator 
        System.out.println("a / b = " + (a / b));

        // Modulo Operator 
        System.out.println("a % b = " + (a % b));

    }

}
