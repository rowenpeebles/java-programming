class AssignmentOperators {

    public static void main(String[] args) {

        // Create Variables
        int a = 4;
        int var;

        // Assign value using '='
        var = a;
        System.out.println("var using '=' : " + var);

        // Assign using '+='
        var += a;
        System.out.println("var using '+' : " + var);

        // Assign using '*='
        var *= a;
        System.out.println("var using '*=' : " + var);

    }

}
